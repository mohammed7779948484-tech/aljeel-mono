import Link from 'next/link'
import { Card, CardContent, CardHeader, CardTitle } from '@/components/ui/card'
import { getSmartchatEvalDashboard } from '@/services/server/smartchat-eval'
import { FlaskConical, Gauge, TimerReset, TrendingDown, TrendingUp } from 'lucide-react'

function percent(value: number) {
  return `${Math.round((value || 0) * 100)}%`
}

function num(value: number) {
  return Number(value || 0).toLocaleString()
}

function dateText(value: string) {
  const date = new Date(value)
  if (Number.isNaN(date.getTime())) return value || '-'
  return date.toLocaleString()
}

function deltaBadge(metricKey: string, delta: number) {
  const lowerIsBetter = metricKey === 'hallucinationRiskRate' || metricKey === 'latencyP95'
  const improved = lowerIsBetter ? delta < 0 : delta > 0
  const text = metricKey === 'latencyP95'
    ? `${delta >= 0 ? '+' : ''}${Math.round(delta)} ms`
    : `${delta >= 0 ? '+' : ''}${(delta * 100).toFixed(1)}%`

  return {
    improved,
    text,
    className: improved
      ? 'border-green-300 bg-green-50 text-green-800'
      : 'border-red-300 bg-red-50 text-red-800',
  }
}

export default async function SmartchatEvalPage() {
  const data = await getSmartchatEvalDashboard(20)
  const latest = data.latest

  return (
    <div className="space-y-6">
      <div className="flex flex-wrap items-center justify-between gap-3">
        <div>
          <h1 className="text-3xl font-bold">Smart Chat Evaluation</h1>
          <p className="text-muted-foreground mt-1">
            Offline quality benchmark reports generated by eval dataset runs.
          </p>
        </div>
        <Link
          href="/admin/smartchat-analytics"
          className="inline-flex items-center gap-2 rounded-full border border-border px-3 py-1.5 text-sm text-muted-foreground hover:text-foreground transition-colors"
        >
          <Gauge className="h-4 w-4" />
          Open Live Analytics
        </Link>
      </div>

      {!data.exists || !latest ? (
        <Card>
          <CardContent className="py-8 text-center text-muted-foreground">
            No eval reports found yet. Run <code>npm run eval:smartchat</code> first.
          </CardContent>
        </Card>
      ) : (
        <>
          <div className="grid grid-cols-1 md:grid-cols-2 xl:grid-cols-4 gap-4">
            <Card>
              <CardHeader className="flex flex-row items-center justify-between pb-2">
                <CardTitle className="text-sm text-muted-foreground">Intent Accuracy</CardTitle>
                <TrendingUp className="h-4 w-4 text-secondary" />
              </CardHeader>
              <CardContent>
                <div className="text-3xl font-bold">{percent(latest.summary.intentAccuracy)}</div>
              </CardContent>
            </Card>
            <Card>
              <CardHeader className="flex flex-row items-center justify-between pb-2">
                <CardTitle className="text-sm text-muted-foreground">Source Coverage</CardTitle>
                <FlaskConical className="h-4 w-4 text-secondary" />
              </CardHeader>
              <CardContent>
                <div className="text-3xl font-bold">{percent(latest.summary.sourceCoverageRate)}</div>
              </CardContent>
            </Card>
            <Card>
              <CardHeader className="flex flex-row items-center justify-between pb-2">
                <CardTitle className="text-sm text-muted-foreground">Hallucination Risk</CardTitle>
                <TrendingDown className="h-4 w-4 text-secondary" />
              </CardHeader>
              <CardContent>
                <div className="text-3xl font-bold">{percent(latest.summary.hallucinationRiskRate)}</div>
              </CardContent>
            </Card>
            <Card>
              <CardHeader className="flex flex-row items-center justify-between pb-2">
                <CardTitle className="text-sm text-muted-foreground">Latency p95</CardTitle>
                <TimerReset className="h-4 w-4 text-secondary" />
              </CardHeader>
              <CardContent>
                <div className="text-3xl font-bold">{num(latest.summary.latencyMs.p95)} ms</div>
              </CardContent>
            </Card>
          </div>

          <div className="grid grid-cols-1 xl:grid-cols-2 gap-4">
            <Card>
              <CardHeader>
                <CardTitle>Latest Run</CardTitle>
              </CardHeader>
              <CardContent className="space-y-2 text-sm">
                <div>Dataset: <span className="font-medium">{latest.summary.datasetName} ({latest.summary.datasetVersion})</span></div>
                <div>Endpoint: <span className="font-medium break-all">{latest.summary.endpoint}</span></div>
                <div>Cases: <span className="font-medium">{latest.summary.totalCases}</span></div>
                <div>Success: <span className="font-medium">{latest.summary.successCount}</span></div>
                <div>Errors: <span className="font-medium">{latest.summary.errorCount}</span></div>
                <div>Generated: <span className="font-medium">{dateText(latest.createdAt)}</span></div>
                <div>File: <span className="font-medium">{latest.fileName}</span></div>
              </CardContent>
            </Card>

            <Card>
              <CardHeader>
                <CardTitle>Previous Run</CardTitle>
              </CardHeader>
              <CardContent className="space-y-2 text-sm">
                {data.previous ? (
                  <>
                    <div>Dataset: <span className="font-medium">{data.previous.summary.datasetName} ({data.previous.summary.datasetVersion})</span></div>
                    <div>Cases: <span className="font-medium">{data.previous.summary.totalCases}</span></div>
                    <div>Success: <span className="font-medium">{data.previous.summary.successCount}</span></div>
                    <div>Errors: <span className="font-medium">{data.previous.summary.errorCount}</span></div>
                    <div>Generated: <span className="font-medium">{dateText(data.previous.createdAt)}</span></div>
                    <div>File: <span className="font-medium">{data.previous.fileName}</span></div>
                  </>
                ) : (
                  <div className="text-muted-foreground">No previous report available yet.</div>
                )}
              </CardContent>
            </Card>
          </div>

          <Card>
            <CardHeader>
              <CardTitle>Run Comparison (Latest vs Previous)</CardTitle>
            </CardHeader>
            <CardContent>
              {data.deltas.length === 0 ? (
                <div className="text-sm text-muted-foreground">Comparison appears after at least 2 reports.</div>
              ) : (
                <div className="space-y-3">
                  {data.deltas.map((row) => {
                    const badge = deltaBadge(row.key, row.delta)
                    return (
                      <div key={row.key} className="flex flex-wrap items-center justify-between gap-3 border-b pb-3 last:border-0">
                        <div>
                          <div className="font-medium">{row.label}</div>
                          <div className="text-xs text-muted-foreground">
                            Current: {row.key === 'latencyP95' ? `${Math.round(row.current)} ms` : percent(row.current)} | Previous: {row.key === 'latencyP95' ? `${Math.round(row.previous)} ms` : percent(row.previous)}
                          </div>
                        </div>
                        <span className={`inline-flex rounded-full border px-2.5 py-1 text-xs font-medium ${badge.className}`}>
                          {badge.text}
                        </span>
                      </div>
                    )
                  })}
                </div>
              )}
            </CardContent>
          </Card>

          <Card>
            <CardHeader>
              <CardTitle>Recent Eval Reports</CardTitle>
            </CardHeader>
            <CardContent>
              <div className="space-y-3">
                {data.reports.map((report) => (
                  <div key={report.fileName} className="flex items-center justify-between border-b pb-3 last:border-0">
                    <div>
                      <div className="font-medium">{report.fileName}</div>
                      <div className="text-xs text-muted-foreground">
                        {dateText(report.createdAt)} | Intent {percent(report.summary.intentAccuracy)} | Source {percent(report.summary.sourceCoverageRate)}
                      </div>
                    </div>
                    <div className="text-xs text-muted-foreground">{report.summary.totalCases} cases</div>
                  </div>
                ))}
              </div>
            </CardContent>
          </Card>
        </>
      )}
    </div>
  )
}
